package org.n52.series.dwd.rest;

public abstract class Alert {

    private String stateShort;

    private String regionName;

    private String description;

    private long end;

    private long start;

    private String event;

    private String instruction;

    private int altitudeStart;

    private int altitudeEnd;

    private String headline;

    private int type;

    private int level;

    private String state;

    public String getStateShort() {
        return stateShort;
    }

    public void setStateShort(String stateShort) {
        this.stateShort = stateShort;
    }

    public String getRegionName() {
        return regionName;
    }

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public long getEnd() {
        return end;
    }

    public void setEnd(long end) {
        this.end = end;
    }

    public long getStart() {
        return start;
    }

    public void setStart(long start) {
        this.start = start;
    }

    public String getEvent() {
        return event;
    }

    public void setEvent(String event) {
        this.event = event;
    }

    public String getInstruction() {
        return instruction;
    }

    public void setInstruction(String instruction) {
        this.instruction = instruction;
    }

    public int getAltitudeStart() {
        return altitudeStart;
    }

    public void setAltitudeStart(int altitudeStart) {
        this.altitudeStart = altitudeStart;
    }

    public int getAltitudeEnd() {
        return altitudeEnd;
    }

    public void setAltitudeEnd(int altitudeEnd) {
        this.altitudeEnd = altitudeEnd;
    }

    public String getHeadline() {
        return headline;
    }

    public void setHeadline(String headline) {
        this.headline = headline;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public abstract String getAlertType();

    public enum AlertTypes {
        warning, vorabInformation;
    }

}
